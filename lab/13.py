import turtle as t
t.shape('turtle')
t.circle(100)
t.color('yellow')
t.begin_fill()
t.circle(100)
t.end_fill()
t.penup()
t.goto(-40,150)
t.pendown()
t.color('black')
t.circle(10)
t.color('blue')
t.begin_fill()
t.circle(10)
t.end_fill()
t.penup()
t.goto(40,150)
t.pendown()
t.color('black')
t.circle(10)
t.color('blue')
t.begin_fill()
t.circle(10)
t.end_fill()
t.penup()
t.goto(0,120)
t.right(90)
t.pendown()
t.color('black')
t.width(10)
t.forward(40)
t.color('red')
t.penup()
t.goto(40, 60)
t.pendown()
t.right(0)
t.circle(-40,180)